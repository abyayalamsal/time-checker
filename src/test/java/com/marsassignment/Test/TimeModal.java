package com.marsassignment.Test;

public class TimeModal {

	private String city;
	private String time;	
	
	public TimeModal(String city, String time) {
		this.city = city;
		this.time = time;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	
	
}
